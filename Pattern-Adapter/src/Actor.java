
public class Actor implements PerformanceArtist {
	public Actor(){}
	public void perform() {
		System.out.println("To be or not to be? That is the question.");
	}
	public void showEgo() {
		System.out.println("That thing I was in was only good because I was in it.");
	}
	public void fireManager() {
		System.out.println("You're fired!");
	}
}